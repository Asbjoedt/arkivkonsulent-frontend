            Cumulus – Digital Asset Management
            Cumulus er et digitalt repository til assets (bevaringsværdige filer) med mulighed for registrering, søgning, opbevaring (server eller sky) og offentlig adgang til filerne. Systemet kan fungere som online formidlingsarkiv til billeder, videoer, dokumenter, lyd o.a. for historiske arkiver. Attention Solutions er den danske forhandler af Cumulus.

            Danske, digitale billedarkiver: Nationalmuseet, København, arkiv.dk, fynskebilleder.dk, Aalborg, Bornholmske Billeder, Aarhus, Danmarks Jernbanemuseum.

            Kommunernes Landsforenings Emnesystematik (KLE)
