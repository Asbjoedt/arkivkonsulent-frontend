            DROID
            Et frit værktøj udviklet af The National Archives i England til at kortlægge filformater herunder versioner, alder, størrelse og hvornår filen sidst er ændret. Dataene som redskabet generer kan herefter anvendes aktivt i den digitale bevaringsplanlægning, så du kan holde styr på, hvor mange filer af et bestemt filformat under risiko for teknologisk forældelse, som der eksisterer i jeres arkiv.

            It-systemer til standardgodkendt digital bevaring: Archivematica, RODA, Preservica, Arkivum, ESSArch.
            Et it-system til digital bevaring skal kunne håndtere registrering, bevaring, fremfinding og tilgængeliggørelse samt kontrol og bevaringsplanlægning af digitale arkivalier. Hvilke niveauer og forbindelser, de enkelte funktioner eksisterer på, kan dog variere, og tilgængeliggørelse behøver f.eks. ikke de facto betyde, at it-systemet har brugerrettede interfaces til arkivadgang.

            International Journal of Digital Curation – videnskabeligt tidsskrift for digital kuratering (og bevaring)
            Tidsskriftet er udgivet af University of Edinburgh’s Digital Curation Centre. Artikler er udgivet under følgende vilkår: “Papers address policy, strategic, operational, experimental, infrastructural, or tool-based aspects of digital data and other objects of value to research, cultural heritage or society. Peer-reviewed papers cover original research supported by significant evidence. General articles are descriptive of some relevant event, activity, research project in progress, or approach. They may present a proposal for an architecture, an implementation, a service, or a project, for example. General articles may also be opinion pieces, reviews, or surveys of existing work in some particular area.”

