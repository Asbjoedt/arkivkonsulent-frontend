            Netværket Elektronisk Arkivering (NEA) og KOMDA – Kommunale konsulentfællesskaber
            De kommunale konsulentfællesskaber yder støtte til medlemskommuner i det afleveringsforberedende arbejde, test af arkiveringsversioner og opbevaring og tilgængeliggørelse af arkiverede myndighedsdata. Fællesskaberne varetager de facto den digitale arkivering for medlemskommunerne, der betaler et årligt kontingent for ydelsen. Fællesskaberne eksisterer, fordi der kan være ressource- eller kompetencemangel i de enkelte kommuner til at løfte opgaven selvstændigt, og fordi der er besparelser at hente ved at gå sammen om klyngeafleveringer af data fra it-systemer, som flere kommuner benytter f.eks. KMDs systemer. For at være medlem er det nødvendigt for en kommune at oprette et §7-arkiv i henhold til den danske arkivlov.

